package moim.letter.model;

public class LetterPaging {

	private String letTomem;
	private int letStart;
	private int letEnd;


	public String getLetTomem() {
		return letTomem;
	}
	public void setLetTomem(String letTomem) {
		this.letTomem = letTomem;
	}
	public int getLetStart() {
		return letStart;
	}
	public void setLetStart(int letStart) {
		this.letStart = letStart;
	}
	public int getLetEnd() {
		return letEnd;
	}
	public void setLetEnd(int letEnd) {
		this.letEnd = letEnd;
	}
}
